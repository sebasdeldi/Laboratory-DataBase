    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Funciones f = new Funciones();
        String nombre = EditNombre.getText();
        String apellido = EditApellido.getText();
        String telefonoTxt = EditTelefono.getText();
        int telefono = parseInt(telefonoTxt);
        String idTxt = EditId.getText();
        int cedula = parseInt(idTxt);
        String direccion = EditDireccion.getText();
        String correo = EditCorreo.getText();
        
        
        f.editar("UPDATE cliente\n" +
        "SET nombre="+f.agregarComillas(nombre)+",apellido= "+f.agregarComillas(apellido)+",email= "+f.agregarComillas(correo)+","+"email= "+f.agregarComillas(correo)+","+"direccion= "+f.agregarComillas(direccion)+","+"telefono= "+telefono+"\n" +
        "WHERE cedula= "+cedula+";", "cliente");
        
        
        String select = "SELECT * FROM cliente;";
        f.poblarTabla(select,Clientes.getTabla());
        System.out.println("actualizado satisfactoriamente");
    }//GEN-LAST:event_jButton1ActionPerformed

